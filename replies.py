	'sure it is safe and that it works.\n\n'
	'*Testing*\n'
	'A new vaccine is first tested in animals to check that it is safe '
	'and to show that it can prevent the disease.\n\n'
	'Then scientists do 3 phases of human clinical trials:\n\n'
	'In phase I, a small number of volunteers get the vaccine. The vaccine '
	'is tested for safety and to make sure it kicks off an immune '
	'response. Researchers also work out the right dosage.\n\n'
